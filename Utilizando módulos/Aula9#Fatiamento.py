#Fatiamento
frase = "Curso em vídeo python"
#imprimendo apenas o caracter 9 partindo do Zero
print(frase[9])
#Imprimendo començando de uma posição até outra
print(frase[9:14])
print(frase[9:21])
#Imprimindo pulando de dois em dois
print(frase[9:21:2])
#Imprimindo até o caracter 5 da frase
print(frase[:5])
#Imprimendo do caracter 15 até o final da string
print(frase[15:])
#Imprimendo começando do 9 e vai até o final de 3 em 3
print(frase[9::3])



